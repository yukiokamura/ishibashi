    <p class="medical-contents-item-contents-tit">腰痛</p>
    <p class="medical-contents-item-contents-main-des">腰痛は怖くない。<br>手術が必要な腰痛はほとんどありません。<br>痛みが強いときには無理は禁物ですが、過度の安静もよくありません。<br>適切なリハビリ（運動療法）が大事です。<br>レッドフラッグサイン（安静時の痛み、発熱、下肢の痺れなど）の見極めが大事。</p>
    <p class="medical-contents-item-contents-des">腰椎椎間板ヘルニア、腰部脊柱管狭窄症、骨粗鬆症、化膿性脊椎炎、悪性腫瘍、非特異的腰痛</p>
    <p class="medical-contents-item-contents-tit">膝の痛み</p>
    <p class="medical-contents-item-contents-main-des">中高年の膝痛の主な原因は、関節軟骨の摩耗と半月板の傷です。<br>ヒアルロン酸の注射と下肢の筋力を鍛える事で痛みを治す事が出来ます。<br>病院でのリハビリや自宅での運動を指導します。</p>
    <p class="medical-contents-item-contents-des">変形性膝関節症、半月板損傷、脆弱性骨折、骨壊死、関節リウマチ</p>
    <p class="medical-contents-item-contents-tit">首、肩の痛み、肩こり</p>
    <p class="medical-contents-item-contents-main-des">肩の周りの筋肉をほぐし、肩の運動をする事で肩関節の機能を回復させます。<br>肩甲骨を動かせるようになる事がポイントです。<br>中高年の場合、腱板に傷があることが多いので、ヒアルロン酸の注射も併用します。</p>
    <p class="medical-contents-item-contents-des">頸椎椎間板ヘルニア、頸椎症、後縦靭帯骨化症、五十肩、肩関節周囲炎、腱板損傷、四十肩、結晶誘発性肩関節炎、石灰化</p>
    <p class="medical-contents-item-contents-tit">骨粗鬆症</p>
    <p class="medical-contents-item-contents-main-des">１５年前から新しい薬が次々と開発され、骨粗鬆症の治療は大きく進歩しました。<br>早期に治療を開始する事で骨折のリスクを５０％以上減らします。<br>転倒での骨折や、いつの間にか骨折を生じていれば、それは骨粗鬆症です。<br>２０〜４４才の平均骨密度の７０％以下は骨粗鬆症です。治療の目的は骨折の予防です。<br>月１回の飲み薬、注射、半年に１回の注射などがあります。<br>骨折を早く治す注射もあります。</p>
